
public class StringApp {

	public static void main(String[] args) {
		
		System.out.println("Hello World"); // string 문자가 모인 문자열
		System.out.println('H'); // Character 문자 하나 Charactor 모이면 String ' 작은따옴표
		System.out.println("H"); // 하나지만 문자열
		
		System.out.println("Hello "
				+ "World");
		
		// new line  \n 줄바꿈 하는 명령어 
		System.out.println("Hello \nWorld");  // Hello 줄 바꾸고 World
		
	
		//escape      \블라블라\  \ 사이 문자열을 문자로 인식하게 함
		System.out.println("Hello \"World\""); // Hello "World"
	}

}
